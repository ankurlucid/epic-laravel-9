   function templateLoad(){

      var data = JSON.parse(sessionStorage.getItem("templateData"));
      var html3 = "";
      var gb_change_life_reason = data.goal_template.gb_change_life_reason.split(",");
      $(".template-life-change").html("");
      
      var gb_change_life_reason_other = null;
      var life_change_class = 'life_change_reason';
      var template_id = data.goal_template.id;

      

      for (var i = 0; i < gb_change_life_reason.length; i++) {

         var radio_val = "";

         var preselect = preSelection(template_id,gb_change_life_reason[i]);
         if(preselect == true){
            radio_val = "checked";
         }

         if (gb_change_life_reason[i].trim() == "Other") {
            gb_change_life_reason[i] = gb_change_life_reason[i].trim();   

            life_change_class = "other_life_change_reason";
            if (gb_change_life_reason_other != null) {
               html3 =
                  '<textarea rows="7" class="form-control" id="gb_change_life_reason_other" name="gb_change_life_reason_other">' + gb_change_life_reason_other + '</textarea>';
            } else {
               html3 =
                  '<textarea rows="7" class="form-control hidden" id="gb_change_life_reason_other" name="gb_change_life_reason_other"></textarea>';
            }
         }

         $(".template-life-change").append(
            '<div class="form-group">\
               <label class="container_check version_2">' +
            gb_change_life_reason[i] +'\
               <input type="checkbox" class="' +
            life_change_class +
               '" name="life-change[]" required value="' +
            gb_change_life_reason[i] +
               '" ' +
            radio_val + '>\
               <span class="checkmark"></span>\
               ' +
            html3 +
            "\
            </label>\
            </div>"
         );
      }
   }

