<script type="text/javascript">
   $("#gb_change_life_reason_other").hide();
   $(document).on("click","#other",function(e) {
       if($(this).is(":checked")) {
           $("#gb_change_life_reason_other").show(300);
       } else {
           $("#gb_change_life_reason_other").hide(200);
       }
   });

  $(document).ready(function() {
     loadFormData();
  });

  function loadFormData(){
     var current_step = $('.step').data('step');
     $.ajax({
         url: public_url + 'goal-buddy/load-form-data',
         type: "POST",
         data: {'current_step': current_step},
         success: function (data) {
            console.log(data);
            if(data.data && data.data['life-change'] != undefined && data.data['life-change'].length > 0){
              data.data['life-change'].map(data => {
                $("input[type=checkbox][value='"+data+"']").attr("checked",true);
              });
            }
            if(data.data && data.data.gb_change_life_reason_other != undefined && data.data.gb_change_life_reason_other != ""){
               $('#gb_change_life_reason_other').show();
               $('#gb_change_life_reason_other').attr('required',true);
               // $('#gb_change_life_reason_other').attr('placeholder','Describe your achievement Here...');
               $('#gb_change_life_reason_other').text(data.data.gb_change_life_reason_other);
            }
          }
     });
  }

</script>